package co.handk.schema.enums;

/**
 * 前端组件类型枚举
 */
public enum FieldType {

    /** 输入框 */
    INPUT,

    /** 多行文本 */
    TEXTAREA,

    /** 密码框 */
    PASSWORD,

    /** 数字输入 */
    NUMBER,

    /** 下拉选择 */
    SELECT,

    /** 单选 */
    RADIO,

    /** 多选 */
    CHECKBOX,

    /** 开关 */
    SWITCH,

    /** 日期 */
    DATE,

    /** 日期时间 */
    DATETIME,

    /** 上传 */
    UPLOAD,

    /** 树选择 */
    TREE_SELECT
}